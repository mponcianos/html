# INDICE STRING

texto = "Você está no curso de Recuperação de Informação com Linguagem Python"
print(type(texto))

# imprime um elemento indicado pelo posição do índice
print(texto[10])

# slice incluindo o primeiro e excluindo o último índice
print(texto[10:14])

# imprime do indice 10 até o final
print(texto[10:])

# imprime na ordem inversa
print(texto[::-1])

# imprime o terceiro caracter de tras pra frente
print(texto[-3])

# imprime um intervalo (ver o range())
print(texto[10:20:2])
